#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ret;
	size_t	i;
	size_t	slen;

	slen = ft_strlen(s);
	if (len > slen)
		len = slen;
	if (start > slen)
		len = 0;
	ret = malloc(len + 1);
	if (s == NULL || ret == NULL)
		return (NULL);
	i = 0;
	while (i < len && s[start + i])
	{
		ret[i] = s[start + i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
